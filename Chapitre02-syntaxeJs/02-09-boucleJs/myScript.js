// les boucles while
var i=1;
while(i<10){
    console.log(i);
    i++;
}
console.log("Fin de boucle while");

// les boucles while
var j=1;
do{
    console.log(j);
    j++;
}while(j<10);
console.log("Fin de boucle do while");

// boucle for
for(var cpt=1; cpt<10;cpt++){
    console.log(cpt);
}
console.log("Fin de boucle for");